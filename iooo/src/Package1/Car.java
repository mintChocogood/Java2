package Package1;

public class Car {
//	1. 필드
	private int speed;
	private boolean stop;
	
	// 2. 생성자
	public Car() {
		;;
	} // 기본 생성자

	public int getSpeed() {
		return speed;
	}

	public void setSpeed(int speed) {
		this.speed = speed;
	}

	public boolean isStop() {
		return stop;
	}

	public void setStop(boolean stop) {
		this.stop = stop;
	}
	
	
	
} // end class
